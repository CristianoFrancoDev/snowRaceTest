package classi;

public class Attrezzature {
    private String articolo;

    public Attrezzature(String articolo) {
        this.articolo = articolo;
    }

    public String getArticolo() {
        return articolo;
    }

    public void setArticolo(String articolo) {
        this.articolo = articolo;
    }

    @Override
    public String toString() {
        return "Attrezzature{" +
                "articolo='" + articolo + '\'' +
                '}';
    }
}
